# What is data types in python?
# Data types in Python refer to the classification or categorization of data items.
# They define the type of value that a variable can hold and determine the operations that can be performed on that data.
# Python has several built-in data types, including:
# 1. Numeric Types: int (integer), float (floating-point number), complex (complex number)
# 2. Text Types: str (string), list (ordered collection), tuple (ordered, immutable collection)
# 3. Mapping Type: dict (dictionary, key-value pairs)
# 4. Set Types: set (unordered collection of unique elements), frozenset (immutable set)
# 5. Boolean Type: bool (True or False)
# 6. None Type: NoneType (represents the absence of a value)
 
