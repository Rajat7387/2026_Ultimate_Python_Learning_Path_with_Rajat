# what is comments in python? 
# Comments in Python are lines of text that are not executed as part of the program.
# They are used to provide explanations, notes, or annotations within the code to improve readability and understanding.
# In Python, comments are created by placing a hash symbol (#) before the text of the comment.

#What is variables in python?
# Variables in Python are used to store data values. They act as containers that hold information which 
# can be referenced and manipulated throughout the program.
# In Python, you can create a variable by simply assigning a value to a name using the equals sign (=).

# rulles of the variable naming in python:
# 1. A variable name must start with a letter (a-z, A-Z) or an underscore (_).
# 2. The rest of the variable name can contain letters, numbers (0-9), and underscores (_).
# 3. Variable names are case-sensitive, meaning that "myVariable" and "myvariable" are considered different variables.
# 4. Variable names cannot be the same as Python reserved keywords (e.g., if, else, while, for, etc.).
# 5. Variable names should not contain spaces. Instead, use underscores (_) to separate words (e.g., my_variable).
# 6. Variable names should be descriptive and meaningful to enhance code readability.

# good variable names:
